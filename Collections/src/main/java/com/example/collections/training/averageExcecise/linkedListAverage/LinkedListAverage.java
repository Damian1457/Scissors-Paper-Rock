package com.example.collections.training.averageExcecise.linkedListAverage;

public class LinkedListAverage {
    private String name;
    private int year;

    public LinkedListAverage(String name, int year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }
}
